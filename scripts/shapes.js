function generateSquare(pos1, pos2)
{
    let delta = getPosDelta(pos1, pos2);
    let newPoint;
    if (Math.abs(delta.x) < Math.abs(delta.y))
    {
        newPoint = {
            x: pos2.x,
            y: pos1.y
        }
    } else {
        newPoint = {
            x: pos1.x,
            y: pos2.y
        }
    }

    return generateRect(pos1, newPoint);
}

